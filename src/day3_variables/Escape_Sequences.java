package day3_variables;

public class Escape_Sequences {

		public static void main(String[] args) {
			
			// \n converts print statement into a println
			System.out.print("Hello, Students!\n");
			System.out.print("Hello,\n Teachers!");
			
			System.out.println("\tHappy Sunday");
			
			
			// \\ to output a backslash
			System.out.println("\\");
			
			// "Harry Potter" is a great book!
			
			// \" - to output double quote in a text
			System.out.println("\"Harry Potter\" is a great book!");
			
			// \' to output a single quotation mark
			System.out.println("\'");
			
			
	
					
			
		}
}
