package day3_variables;

public class Output_Statements {

	public static void main(String[] args) {
		
		// prints something and then transitions to a new line
		System.out.println("Happy Sunday!") ;
		
		// prints something and doesn't go to a new line
		System.out.print("Prime Tech ");
		
		// this statement prints on the same line as Prime Tech
		System.out.print("02/14/2021");
		
	}

}
