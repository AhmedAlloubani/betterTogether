package day6_operators2;

public class Unary_Tasks {
	public static void mani(String[] args) {
		
		// Task 1
		int x = 2;         // x = 2;
		int y = x++;       // y = 2; x = 3
		System.out.println(y);  // y = 2
		
		
		// Task 2
		int x1 = 2;               // x1 = 2
		System.out.println(x1++); // x1 = 2
		System.out.println(x1);   // 3
		
		
	}

}
