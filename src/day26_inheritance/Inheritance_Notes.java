package day26_inheritance;

public class Inheritance_Notes {
	
	/*
	 * public class Animal {
	 * 		
	 * 		int age;
	 * 
	 * 		public void run() {
	 * 			Print("Animal is running!"
	 * 		}
	 * 
	 * 		public void sleep() {
	 * 			Print("Animal is sleeping!");
	 * 		}
	 * 
	 * public class Cat extends Animal {        Child class/Sub class
	 * 
	 * 	}
	 * 
	 * Java allows for classes to extend another class. It will give the subclass an access to the methods and variables 
	 * of the super class (parent class). The main point of inheritance is reusability - we don't have to provide the methods and variables
	 * within the subclass that are already available within the super class and applicable to any class that can potentially extend the 
	 * super class.
	 * 
	 * To extend a class we need to use 'extend' keyword.
	 * 
	 * Subclasses don't have access to private fields and methods.
	 * 
	 */

}
