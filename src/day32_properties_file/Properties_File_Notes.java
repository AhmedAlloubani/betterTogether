package day32_properties_file;

public class Properties_File_Notes {
	
	/*
	 * Properties File: is the type of file that lets you set up a key-value store.
	 * 
	 * 
	 * A key-value pair
	 * 
	 * text - 5
	 * apple - 7
	 * 
	 * fruit - apple
	 * vegetable - pumpkin 
	 * 
	 * Example of the properties file setup: 
	 * 
	 * timeOfDay=afternoon
     * driver=chrome
     * systemProperty=/home/folder/chromedriver.exe
     * 
     * - If java finds that particular key in the properties file, it will give you back the value
     * - If java doesn't find the key, it will return null
     * - default value can be provided in the overloaded version of the getPropert() method, this default
     * value will be called if the key is not in the file.
     * 
     * Summary: properties file is used as a central point of control and data store for the test automation framework
	 * 
	 */

}
